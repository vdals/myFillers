#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char			*dup;
	unsigned int	i;

	i = 0;
	while (s1[i])
		i++;
	if (!(dup = (char *)malloc(sizeof(*dup) * (i + 1))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
